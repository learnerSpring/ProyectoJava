    public boolean valida_datos(){    
      if (tf_fecha.getText().isEmpty() || 
          tf_sacerdote.getText().isEmpty() || tf_novio.getText().isEmpty() || tf_novia.getText().isEmpty() ||
          tf_testigos.getText().isEmpty()  
              )
          return false;
      else 
          return true;
    }
